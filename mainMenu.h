void DrawMainMenu(void)
{  
  vram_adr(NTADR_A(0, 1));
  
  vram_put(0x01);
  
  vram_adr(NTADR_A(1, 1));
  
  vram_write(MainMenuScreen, sizeof(MainMenuScreen));
  
  for(i = 0; i < 31; i++)
  {
    vram_put(1);
  }
  
  vram_adr(NTADR_A(10, 14));
  vram_write("Select a map", 12);
  
  j = 0;
  
  for(i = 0; i < sizeof(MapNames); i++)
  {
    if(MapNames[i] == '\0')
    {
      ++j;
      
      if(j > MapCount) break;
      
      vram_adr(NTADR_A(12, 15 + (j * 2)));
    }
    else
    {
      vram_put(MapNames[i]);
    }
  }
  
  
  
  ppu_on_all();
}

void MainMenu(void)
{
  DrawMainMenu();
  
  while(1)
  {
    ppu_wait_frame();
    
    oam_off = 0;
    
    oam_off = oam_spr(78, ((17 + map * 2) * 8) - 1, MapIcons[map], 0, oam_off);
    oam_off = oam_spr(86, ((17 + map * 2) * 8) - 1, MapIcons[map], 0xc0, oam_off);
    
    if(oam_off != 0) oam_hide_rest(oam_off);
    
    PrevPad = gamepad;
    
    gamepad = pad_poll(0) | pad_poll(1);
    
    if(gamepad&PAD_SELECT && !(PrevPad&PAD_SELECT)) ++map;
    
    if(map >= MapCount) map = 0;
    
    if(gamepad&PAD_START && !(PrevPad&PAD_START)) break;
  }
}
