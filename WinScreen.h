void WinScreen(void)
{  
  oam_clear();
  
  ppu_off();
  
  pal_all(WINSCREENPALETTE);
  
  vram_adr(NTADR_A(0, 1));
  vram_put(1);
  
  vram_adr(NTADR_A(1, 1));
  
  vram_write(Screen, sizeof(Screen));
  
  if(GameState&P2_CAPD)
  {
    for(i = 0; i < TileSizeX_Blue; ++i)
    {
      vram_adr(NTADR_A(5, 5 + i));
      vram_write(Blue[i], TileSizeY);
    }
  }
  else
  {
    for(i = 0; i < TileSizeX_Red; ++i)
    {
      vram_adr(NTADR_A(5, 4 + i));
      vram_write(Red[i], TileSizeY);
    }
  }
  
  ppu_on_all();
}
